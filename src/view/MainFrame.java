    public void init() {
        if (Kairos.getSesionDate() != null) {
            this.jLabel1.setText("Grupos actualizados el dia " + sdf.format(Kairos.getSesionDate()));
        } else {
            this.jLabel1.setText("Grupos actualizados el dia --/--/--  --:--");
        }
        this.buttonsPane.removeAll();
        this.buttonsPane.add(Box.createRigidArea(new Dimension(15, 0)));

        for (Asignatura mat : Kairos.getSubjects()) {

            JPanel panel = new JPanel();
            panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
            panel.add(mat.getLabel());

            for (Group grup : mat.getGrupos()) {
                Button boton = new Button(grup, mat);
                panel.add(boton);
                panel.add(boton.getProgressBar());

                boton.addActionListener(new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent ae) {
                        Button bot = (Button) ae.getSource();
                        if (bot.isSelected()) {
                            if (bot.getMateria().isSelected()) {
                                bot.setSelected(false);
                                for (Button boton : bot.getMateria().getButtons()) {
                                    if (boton.isSelected()) {
                                        boton.doClick();
                                        break;
                                    }
                                }
                                bot.setSelected(true);
                            }
                            bot.getMateria().setSelected(true);
                        } else {
                            bot.getMateria().setSelected(false);
                        }
                        selectButton(bot);
                        addBloq(bot);
                    }
                });
            }
            panel.add(Box.createVerticalGlue());
            this.buttonsPane.add(panel);
            this.buttonsPane.add(Box.createHorizontalGlue());
            this.buttonsPane.add(Box.createRigidArea(new Dimension(15, 0)));
            this.buttonsPane.validate();
        }
    }

    private void addBloq(Button boton) {
        Group grupo = boton.getGrupo();
        for (Block bloque : grupo.getHorario()) {
            String value = null;
            if (boton.isSelected()) {
                value = bloque.getSalon() + ": " + boton.getMateria().getNombre();
            }
            for (int j = bloque.getHora(); j < bloque.getHoraFin(); j++) {
                jTable1.setValueAt(value, j - 6, bloque.getDia());
            }
        }
    }

    private void clearSelection() {
        for (Asignatura mat : Kairos.getSubjects()) {
            if (mat.isSelected()) {
                for (Button bot : mat.getButtons()) {
                    if (bot.isSelected()) {
                        bot.doClick();
                        break;
                    }
                }
            }
        }
    }

